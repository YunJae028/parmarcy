package com.example.parmarcy.vo;

public class parmarcyVo {
    private String dutyName;
    private String wgs84Lat;
    private String wgs84Lon;
    private String checkopen;
    private String hpid;

    public String getDutyName() {
        return dutyName;
    }

    public void setDutyName(String dutyName) {
        this.dutyName = dutyName;
    }

    public String getWgs84Lat() {
        return wgs84Lat;
    }

    public void setWgs84Lat(String wgs84Lat) {
        this.wgs84Lat = wgs84Lat;
    }

    public String getWgs84Lon() {
        return wgs84Lon;
    }

    public void setWgs84Lon(String wgs84Lon) {
        this.wgs84Lon = wgs84Lon;
    }

    public String getCheckopen() {
        return checkopen;
    }

    public void setCheckopen(String checkopen) {
        this.checkopen = checkopen;
    }

    public String getHpid() {
        return hpid;
    }

    public void setHpid(String hpid) {
        this.hpid = hpid;
    }
}
